public class PrintingChoicesEscapes {
	public static void main( String[] args ) {
		System.out.print( "Alpha" );
		System.out.print( "\nBravo" );

		System.out.print( "\nCharlie" );
		System.out.print( "\nDelta" );
		System.out.print( "" );

		System.out.print( "\nEcho" );
		System.out.print( "\nFoxtrot" );

		System.out.print( "\nGolf" );
		System.out.print( "\nHotel" );
		System.out.print( "\n " );
		System.out.print( "India\n" );

		System.out.println();
		System.out.print( "This" + " " + "is" + " " + "a" + " test.\n" );
		System.out.print( "I am" + " " + "learning" + " " + "java the" + " " + "hard way!\n");
		
	}
}